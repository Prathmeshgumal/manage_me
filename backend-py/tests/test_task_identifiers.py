import pytest


@pytest.mark.asyncio
async def test_project_key_autogenerated(auth_client):
    client, _ = auth_client
    p = (await client.post("/projects", json={"name": "Manageme"})).json()
    assert p["key"] == "MAN"


@pytest.mark.asyncio
async def test_custom_key_and_uniqueness(auth_client):
    client, _ = auth_client
    p = (await client.post("/projects", json={"name": "Manageme", "key": "mam"})).json()
    assert p["key"] == "MAM"  # normalized to uppercase
    # second project can't reuse the key
    dup = await client.post("/projects", json={"name": "Other", "key": "MAM"})
    assert dup.status_code == 400


@pytest.mark.asyncio
async def test_keys_unique_when_autogenerated(auth_client):
    client, _ = auth_client
    a = (await client.post("/projects", json={"name": "Manageme"})).json()
    b = (await client.post("/projects", json={"name": "Mango"})).json()
    assert a["key"] == "MAN" and b["key"] != "MAN"


@pytest.mark.asyncio
async def test_task_identifiers_sequential_per_project(auth_client):
    client, _ = auth_client
    pid = (await client.post("/projects", json={"name": "Manageme", "key": "MAM"})).json()["id"]

    t1 = (await client.post("/tasks", json={"title": "one", "projectId": pid})).json()
    t2 = (await client.post("/tasks", json={"title": "two", "projectId": pid})).json()
    assert t1["identifier"] == "MAM-001" and t1["number"] == 1
    assert t2["identifier"] == "MAM-002" and t2["number"] == 2


