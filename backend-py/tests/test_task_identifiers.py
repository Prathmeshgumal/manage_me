@pytest.mark.asyncio
async def test_renumber_on_move_between_namespaces(auth_client):
    client, _ = auth_client
    a = (await client.post("/projects", json={"name": "Alpha", "key": "ALP"})).json()["id"]
    b = (await client.post("/projects", json={"name": "Bravo", "key": "BRV"})).json()["id"]

    # seed Bravo so its counter is ahead
    await client.post("/tasks", json={"title": "b1", "projectId": b})
    t = (await client.post("/tasks", json={"title": "movable", "projectId": a})).json()
    assert t["identifier"] == "ALP-001"

    moved = (await client.patch(f"/tasks/{t['id']}", json={"projectId": b})).json()
    assert moved["identifier"] == "BRV-002"

    # move out to no project -> workspace sequence
    loose = (await client.patch(f"/tasks/{t['id']}", json={"projectId": None})).json()
    assert loose["identifier"] == "TSK-001"


@pytest.mark.asyncio
async def test_editing_project_key_updates_task_ids(auth_client):
    client, _ = auth_client
    pid = (await client.post("/projects", json={"name": "Manageme", "key": "MAM"})).json()["id"]
    tid = (await client.post("/tasks", json={"title": "x", "projectId": pid})).json()["id"]

    await client.patch(f"/projects/{pid}", json={"key": "MGM"})
    t = (await client.get(f"/tasks/{tid}")).json()
    assert t["identifier"] == "MGM-001"
